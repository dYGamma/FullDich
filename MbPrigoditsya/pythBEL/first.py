import matplotlib.pyplot as plt

# Данные о времени (месяцы)
time = ['Янв', 'Фев', 'Мар', 'Апр', 'Май', 'Июн', 'Июл', 'Авг', 'Сен', 'Окт', 'Ноя', 'Дек']

# Данные о продажах
sales = [100, 120, 150, 110, 130, 140, 160, 180, 200, 210, 180, 160]

# Построение графика
plt.plot(time, sales)
plt.xlabel('Месяц')
plt.ylabel('Продажи')
plt.title('График продаж по времени')
plt.show()
