#include <iostream>
using namespace std;

int main() {
    double x, y;
    cout << "Введите координаты точки (x, y): ";
    cin >> x >> y;

    if ((x >= -1.0 && x <= 1.0 && y >= 0.0 && y <= 1.0) || (x >= -1.0 && x <= 0.0 && y >= -1.0 && y <= 0.0)) {
        cout << "Точка (" << x << ", " << y << ") попадает в заштрихованную область." << endl;
    }
    else {
        cout << "Точка (" << x << ", " << y << ") не попадает в заштрихованную область." << endl;
    }

    return 0;
}
